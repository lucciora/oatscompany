package com.oatscompany.model;


public class DataVO {
	
	
	private String x_coordinate;
	private String y_coordinate;
	private String address;
	private String sido;
	private String sigungu;
	private String eupdongmyun;
	private String local_id;
	private String selecteddate;
		
	
	public String getX_coordinate() {
		return x_coordinate;
	}
	public void setX_coordinate(String x_coordinate) {
		this.x_coordinate = x_coordinate;
	}
	public String getY_coordinate() {
		return y_coordinate;
	}
	public void setY_coordinate(String y_coordinate) {
		this.y_coordinate = y_coordinate;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getSido() {
		return sido;
	}
	public void setSido(String sido) {
		this.sido = sido;
	}
	public String getSigungu() {
		return sigungu;
	}
	public void setSigungu(String sigungu) {
		this.sigungu = sigungu;
	}
	public String getEupdongmyun() {
		return eupdongmyun;
	}
	public void setEupdongmyun(String eupdongmyun) {
		this.eupdongmyun = eupdongmyun;
	}
	public String getLocal_id() {
		return local_id;
	}
	public void setLocal_id(String local_id) {
		this.local_id = local_id;
	}
	public String getSelecteddate() {
		return selecteddate;
	}
	public void setSelecteddate(String selecteddate) {
		this.selecteddate = selecteddate;
	}

}
